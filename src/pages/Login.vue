<template>
  <v-container fluid fill-height>
    <v-layout align-center justify-center>
      <v-flex xs12 sm8 md4>
        <v-card class="elevation-121" flat>
          <v-toolbar flat>
            <v-toolbar-title>Login</v-toolbar-title>                
          </v-toolbar>
          <v-card-text>
            <v-form>
              <v-text-field prepend-icon="email" name="email" label="Email" type="text" v-model="email" :rules="emailRules"></v-text-field>
              <v-text-field browser-autocomplete="new-password" id="password" prepend-icon="lock" name="password" label="Password" type="password" v-model="password" :rules="passwordRules"></v-text-field>
            </v-form>
          </v-card-text>
          <v-card-actions>
            <v-spacer></v-spacer>
            <v-btn @click="logIn" flat>Login</v-btn>
            <v-btn @click="signUp" flat color="primary">Signup</v-btn>
          </v-card-actions>
        </v-card>
      </v-flex>
    </v-layout>
  </v-container>
</template>

<script>
  export default {
    data: () => ({
      email: '',
      emailRules: [
        v => !!v || 'E-mail is required',
        v => /.+@.+/.test(v) || 'E-mail must be valid'
      ],
      password: '',
      passwordRules: [
        v => !!v || 'Password is required'
      ],
    }),
    methods: {
      logIn () {
        this.$store.dispatch('logIn', {
          email: this.email,
          password: this.password
        })
          .then(() => {
            this.$router.push('/')
          })
      },
      signUp () {
        this.$router.push('/signup')
      }
    }
  }
</script>